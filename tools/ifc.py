# -----------------------------------------------------------------------------
# Type metadata — wire size, natural C++ type, partitioned tag (or UNDEFINED
# for narrow on-wire encodings that don't have a 1:1 IFE::RECOVERY_TAG yet),
# and field kind.
# -----------------------------------------------------------------------------
