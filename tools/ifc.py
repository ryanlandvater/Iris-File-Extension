def write_outputs(schema: Dict[str, Any], out_dir: Path,
                  schema_label: str) -> List[Path]:
    out_dir.mkdir(parents=True, exist_ok=True)
    written: List[Path] = []
    for filename, fn in GENERATORS.items():
        path = out_dir / filename
        path.write_text(fn(schema, schema_label), encoding="utf-8")
        written.append(path)
    return written


def main(argv: List[str]) -> int:
    parser = argparse.ArgumentParser(description="IFE schema-driven codegen.")
    parser.add_argument("--schema", required=True, type=Path,
                        help="Path to the IFE schema JSON.")
    parser.add_argument("--out-dir", required=True, type=Path,
                        help="Directory to write generated headers into.")
    parser.add_argument("--check", action="store_true",
                        help="Verify the on-disk files match what would be regenerated.")
    args = parser.parse_args(argv)

    schema = json.loads(args.schema.read_text(encoding="utf-8"))
    validate_schema(schema)
    # Use just the basename in the generated banner so the comment is stable
    # across different absolute build-tree paths.
    schema_label = args.schema.name

    if args.check:
        with tempfile.TemporaryDirectory() as tmp:
            tmp_path = Path(tmp)
            write_outputs(schema, tmp_path, schema_label)
            mismatches: List[str] = []
            for filename in GENERATORS:
                expected = tmp_path / filename
                actual = args.out_dir / filename
                if not actual.exists() or not filecmp.cmp(expected, actual, shallow=False):
                    mismatches.append(filename)
            if mismatches:
                print(
                    "ifc.py: build-tree generated files are stale: "
                    + ", ".join(mismatches),
                    file=sys.stderr,
                )
                print(
                    "        regenerate with: "
                    f"python3 tools/ifc.py --schema {args.schema} --out-dir {args.out_dir}",
                    file=sys.stderr,
                )
                return 1
            return 0

    written = write_outputs(schema, args.out_dir, schema_label)
    for path in written:
        print(f"ifc.py: wrote {path}")
    return 0


if __name__ == "__main__":
    sys.exit(main(sys.argv[1:]))
